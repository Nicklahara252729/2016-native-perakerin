<h3>Tips umum</h3>
<h5>1. Kantor aliran mana saja</h5>

Gunakan ID Microsoft untuk salinan aliran aplikasi Office Anda ke PC lainnya (Windows 7 atau Windows 8 ). Masuk ke situs web Office untuk menggunakan Office on Demand.<br />
<h5>2. Lokasi Pin</h5>
Simpan folder Anda yang paling sering digunakan ke layar Terbuka dalam aplikasi apapun - menggunakan ikon pin di sebelah kanan buku kerja baru dibuka atau folder.<br />
<h5>3. Simpan ke SkyDrive</h5>
Office 2013 membuat penuh penggunaan akun SkyDrive Anda, dan Anda dapat menyimpan file Anda ke awan, mengaksesnya dari mana saja dan sync mereka di komputer.<br />
<h5>4. Gunakan Flickr dan Facebook gambar
image</h5>
Hubungkan account untuk penurunan gambar dari Facebook atau Flickr
Anjloknya gambar dari Flickr baru dirubah atau Facebook dengan menghubungkan account Anda melalui Gambar Online pilihan di bawah tab Insert.<br />
<h5>5. Mengubah tampilan Office dan tema</h5>
Akses halaman Account dari tab File pada setiap aplikasi untuk mengungkapkan pilihan pilihan untuk mengubah latar belakang dan tema suite.<br />
<h5>6. Zoom dengan jari-jari Anda</h5>
Menggunakan layar sentuh laptop mewah atau tablet dengan Office 2013? Gunakan dua jari untuk memperbesar atau dokumen apapun, seperti pada smartphone Anda.<br />
<h5>7. Menambahkan aplikasi</h5>
Kantor akhirnya bergabung revolusi app - mengunjungi situs Kantor untuk menemukan aplikasi yang Anda dapat berjalan di atas Word, Excel, dan Outlook. Toko diberi label sebagai AS-saja, namun Anda masih dapat memanfaatkannya.<br />
<h5>8. Hapus layar Mulai</h5>
Matikan semua bernyanyi Halaman awal semua menari-baru jika Anda tidak suka
Ketika diluncurkan, Office 2013 aplikasi menampilkan layar Mulai dinamis secara default. Untuk melihat dokumen baru kosong sebagai gantinya, pilih File dan Pilihan, kemudian centang pilihan Start screen pada tab General.<br />
<h5>9. Dapatkan Skyping</h5>
Jika Anda telah mendaftar untuk berbasis langganan 365 rasa Office 2013, Anda mungkin tidak tahu bahwa Anda mendapatkan 60 menit kredit Skype gratis setiap bulan.<br />
<h5>10. Coba instalasi paralel</h5>
Anda mungkin sudah melihat ini, tapi Anda dapat menyimpan versi Office yang berjalan di samping versi 2013, sebaiknya Anda perlu (dengan pengecualian Outlook).<br />
<h5>11. Baca lebih mudah</h5>
Word tampilan baru Baca Mode (di bawah tab View) membuat browsing dokumen lebih mudah, dan mendukung input touchscreen juga.<br />
<h5>12. Cantumkan video </h5>
Drop video YouTube dan bermain mereka dari Firman
Memuat dalam klip video online dari YouTube dan tempat lain menggunakan tombol Video baru online di bawah tab Insert.<br />
<h5>13. Mendefinisikan kata-kata</h5>
Klik kanan pada sebuah kata dan pilih Define untuk memilih kamus dan melihat definisi. Anda bahkan bisa mendapatkan bantuan dengan pengucapan jika Anda membutuhkannya.<br />
<h5>14. Membalas komentar</h5>
Firman 2013 memungkinkan Anda untuk membalas komentar dokumen, dan bahkan menandai mereka sebagai 'done', untuk alur kerja yang halus.<br />
<h5>15. Dapatkan perbatasan yang lebih baik</h5>
Cepat menyalin format perbatasan dengan alat Painter Perbatasan
Tabel perbatasan telah diberikan beberapa cinta di Word 2013, dengan tambahan seperti alat Painter Perbatasan (untuk menyalin format perbatasan), yang berada di bawah bagian Desain tab Table Tools.<br />
<h5>16. Sunting PDF</h5>
Sepenuhnya matang editing PDF akhirnya tiba dengan Word 2013, meskipun Anda mungkin kehilangan beberapa pengaturan tata letak. Dokumen diedit dapat disimpan sebagai file PDF atau DOCX.<br />
<h5>17. Mengubah default</h5>
Dokumen baru menggunakan Calibri dan spasi ganda secara default. Ubah ini dengan mengklik kanan pada ikon stylesheet normal dan memilih Modify.<br />
<h5>18. Manfaat dari keselarasan hidup</h5>
Klik dan drag dan keberatan pada halaman dan Anda akan melihat garis spidol hijau samar muncul, DTP-gaya, memungkinkan Anda untuk berbaris serangkaian elemen lebih mudah.<br />
<h5>19. Coba pembungkus inline </h5>
Gunakan kotak Layout Options pop-up dengan cepat menyesuaikan pengaturan membungkus teks.Teks pilihan pembungkus Word sekarang dapat ditemukan dengan mengklik ikon yang muncul di bagian kanan atas setiap kali gambar dipilih.<br />
<h5>20. Mengatur ulang daftar</h5>
Bukan fitur baru, tapi masih bagus - menggunakan Alt + Shift + maka panah atas atau bawah untuk mengatur ulang item dalam daftar bullet atau nomor, tidak memotong dan menyisipkan diperlukan.<br /><br />
<h3>Excel Tips</h3>
<h5>21. Gunakan Flash Isi</h5>
Berikan Excel 2013 beberapa contoh di atas kolom (seperti nama), dan dapat menyalin ke bawah format. Lihat Microsoft Office Blog untuk panduan.<br />
<h5>22. Mengintip</h5>
Alat Analisis Cepat memungkinkan Anda untuk melihat format dan lebih
Sorot sekelompok sel kemudian klik ikon Analisis Cepat (kanan bawah) untuk melihat mengintip dari grafik yang disarankan, format dan total untuk data.<br />
<h5>23. Lihat tips kunciAlat Analisis Cepat memungkinkan Anda untuk melihat format dan lebih</h5>
Sorot sekelompok sel kemudian klik ikon Analisis Cepat (kanan bawah) untuk melihat mengintip dari grafik yang disarankan, format dan total untuk data.<br />
<h5>23. Lihat tips kunci</h5>
Tekan tombol Alt untuk mengungkapkan cara pintas keyboard
Tekan Alt untuk melihat huruf muncul dari setiap entri pada menu pita - tekan tombol yang relevan untuk mengaktifkan pilihan.<br />
<h5>24. Dapatkan grafik direkomendasikan</h5>
Gunakan Rekomendasi tombol Charts bawah tab Insert untuk melihat pilihan grafik yang Excel berpikir sesuai dengan data yang telah Anda pilih.<br />
<h5>25. Gaya grafik Preview</h5>
Pilih grafik, klik ikon kuas dan Anda dapat melihat pratinjau perubahan gaya grafik dan warna tanpa membuat perubahan.<br />
<h5>26. Menyaring berdasarkan waktu</h5>
Excel 2013 memungkinkan Anda dengan cepat membuat filter waktu untuk setiap kolom tanggal dalam tabel pivot atau grafik (opsi berada di bawah tab PivotTable Tools).<br />
<h5>27. Gunakan panduan rumus</h5>
Excel mencoba untuk menyelesaikan auto-menyarankan gaya rumus Anda secara default, tetapi Anda dapat menonaktifkan ini melalui tab Rumus pada dialog Options, dicapai melalui menu File.<br />
<h5>28. Bermain dengan beberapa jendela</h5>
Excel 2013 Word dan PowerPoint bergabung dalam membuka setiap file di jendela baru (dan jendela taskbar), sehingga lebih mudah untuk mengatur mereka pada layar.<br />
<h5>29. Tambahkan watermark</h5>
Buka Header dan Footer alat untuk menambahkan gambar sebagai watermark
Anda dapat menambahkan watermark ke spreadsheet Anda, meskipun mereka hanya terlihat di Page Layout - hanya memuat gambar sebagai header (Insert / header & Footer).<br />
<h5>30. Apakah penghitungan cepat</h5>
Pilih sekelompok sel untuk melihat nilai rata-rata, jumlah sel dan jumlah dari semua nilai-nilai yang dipilih di sudut kanan bawah.<br /><br />
<h3>PowerPoint Tips</h3>
<h5>31. Lihat pemandangan Presenter</h5>
The Presenter tampilan untuk layar sekunder memberi Anda fleksibilitas lebih jauh, termasuk pilihan untuk memperbesar ke titik-titik tertentu pada slide.<br />
<h5>32. Go widescreen</h5>
Membawa slideshow Anda ke era modern dengan tata letak layar lebar 16:09
Jika layar Anda adalah rasio aspek 16:9 layar lebar, membuat slide Anda 16:09 juga, melalui Ukuran Slide drop-down di bawah tab Design.<br />
<h5>33. Jelajahi Format pane</h5>
Buka Format panel (klik kanan objek dan pilih 'Format Shape') dan secara otomatis menyesuaikan untuk menunjukkan pilihan yang tersedia untuk objek yang sedang dipilih.<br />
<h5>34. Tambahkan musik latar</h5> 
Gunakan Play dalam pilihan Latar Belakang untuk memiliki trek span beberapa slide
Mengatur trek audio untuk bermain selama slideshow Anda dengan Putar di opsi Latar Belakang tab Playback.<br />
<h5>35. Saring foto</h5>
Menambahkan "foto" untuk pencarian Anda dari Pictures Online dialog untuk menyaring gambar garis dan clip-art dari hasil.<br />
<h5>36. Ekspor sebagai video</h5>
Anda dapat menyimpan slideshow PowerPoint Anda seperti video klip
Pilih 'Ekspor' dari menu File untuk menyimpan presentasi PowerPoint sebagai klip MP4, lengkap dengan transisi dan file media.<br />
<h5>37. Tweak tema</h5>
Anda dapat memilih tema dari slideshow tab Design, tetapi Anda juga dapat men-tweak warna, font dan efek yang digunakan melalui kotak Variasi.<br />
<h5>38. Sesuaikan bentuk</h5>
Pilih dua atau lebih bentuk standar kemudian pilih Merge Shapes bawah tab Format untuk menggabungkan mereka ke dalam bentuk kustom.<br />
<h5>39. Pilih warna</h5>
Dari pilihan Fill melalui PowerPoint 2013 Anda dapat menggunakan alat pipet untuk memilih warna naik dari tempat lain dalam presentasi.<br />
<h5>40. Slideshow embed</h5>
Kepala ke upgrade PowerPoint web app untuk menanamkan slideshow Anda di mana saja secara online, lengkap dengan transisi dan animasi.<br /><br />
<h3>Outlook Tips</h3>
<h5>41. Balas inline</h5>
Cobalah - balasan sekarang tinggal tepat di panel baca. Klik tombol Pop Out jika Anda ingin kembali ke cara lama bekerja.<br />
<h5>42. Mengambil tindakan cepat</h5>
Tindakan cepat seperti tanda belum dibaca tersedia dari panel folder
Gunakan salib ikon mengambang untuk menghapus, ikon bendera untuk bendera dan bar biru ke kiri untuk menandai pesan sebagai belum dibaca, langsung dari daftar percakapan. Tindakan ini cepat dapat diterapkan ke beberapa email sekaligus, juga.<br />
<h5>43. Kontak tautan</h5>
Menggabungkan data dari Facebook, LinkedIn, dan sumber-sumber lain, seperti yang Anda dapat di Windows 8 Orang app - pilih Tautan Kontak dari setiap entri pada layar Rakyat untuk melakukannya.<br />
<h5>44. Lihat lebih atau kurang</h5>
Pilih Lihat kemudian Pesan Preview untuk mematikan potongan preview untuk pesan Anda, atau untuk mengurangi mereka untuk satu, dua atau tiga baris.<br />
<h5>45. Buatlah navigasi lebih kompak</h5>
Komponen kunci Outlook (Mail, Kalender, People, Tugas) sekarang meregangkan sepanjang jalan di bagian bawah antarmuka Outlook. Klik tiga titik di strip, pilih Navigasi Options dan centang Compact Navigasi untuk kembali ke pendekatan ikon.<br />
<h5>46. Sesuaikan folder pencarian</h5>
Pilih Cari Folder kemudian New Folder pencarian dari Mail untuk membuat folder pencarian kustom yang secara otomatis update sebagai pertandingan baru datang - Anda dapat menggunakan kata kunci, kontak dan lebih sebagai kriteria.<br />
<h5>47. Preview Link</h5>
Navigasi strip baru dilengkapi dengan fitur pratinjau built-in
Navigasi strip baru Outlook untuk Mail, People, Kalender dan Tugas mencakup fitur preview: membawa lebih dari link untuk melihatnya.<br />
<h5>48. Filter email</h5>
Ada sejumlah pilihan untuk penyaringan melalui email di Outlook
Gunakan opsi Filter Email pada tab Home untuk menyaring email oleh mereka dengan lampiran, menurut tanggal, oleh pengirim atau menggunakan berbagai kriteria lainnya.<br />
<h5>49. Simpan pandangan</h5>
Setelah Anda punya antarmuka Outlook diatur hanya cara Anda seperti itu, simpan konfigurasi tampilan melalui View> Ubah Tampilan> Simpan Current View.<br />
<h5>50. Mengembalikan To Do Bar</h5>
Outlook 2013 menyembunyikan To Do Bar. Pilih opsi To Do Bar di bawah tab View untuk membawanya kembali. 
