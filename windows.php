<h3>Some Tips and Tricks to Use Windows 10</h3>
<h5>1. This is Sparta..!!</h5>
&nbsp;&nbsp;&nbsp;&nbsp;Penggalan kata tersebut mungkin sudah familiar bagi kamu yang menonton film 300 yang menceritakan berbagai kisah tentang Spartan. Tetapi bukan itu yang akan kita bahas disini.
Tweak pertama ini adalah tentang bagaimana cara mengaktifkan Rendering Engine browser terbaru Microsoft Spartan di Internet Explorer Windows 10 Build 9926.
Cukup buka IE lalu ketikkan about:flags. Setelah itu centang Enabled di Enable Experimental Web Platform Features.Kini IE kamu telah menggunakan rendering engine milik Spartan<br />
<img src="Image/other/tweakwindows10_1.jpeg" id="j" /><br /><br />
<h5>2. Buang Jam & Kalender Kuno!</h5>
&nbsp;&nbsp;&nbsp;&nbsp;Windows 10 saat ini masih menggunakan kalender dan jam versi kuno, padahal dia sebenarnya sudah menyertakan versi yang lebih modern.<br />
Untuk membuang versi yang kuni dan menampilkan Jam & Kalender versi modern, buka saja registry editor, lalu browse ke HKEY_LOCAL_MACHINE > Software > Microsoft > Windows > CurrentVersion > ImmersiveShell
Setelah itu klik kanan di area kosong New > Dword (32-bit) Value dan beri nama UseWin32TrayClockExperience.<br />
Sekarang lihat Jam & Kalender yang ada di system tray, katakan selamat tinggal pada versi kuno nya.<br />
<img src="Image/other/tweakwindows10_2.jpeg" id="j" /><br /><br />
<h3>3. Panggil Cortana!</h3>
&nbsp;&nbsp&nbsp;&nbsp;Cortana sudah dihadirkan di Windows 10, dan secara default dia bisa diakses melalui form search yang ada di dalam taskbar.
Namun dengan mengaktifkan opsi “Let Cortana respond when you say Hey Cortana”, maka kamu bisa memanggil Cortana kapanpun kamu inginkan dengan panggilan suara “Hey, Cortana!”<br />
<img src="Image/other/tweakwindows10_3.jpeg" id="j" /><br /><br />
<h3>4. Aktifkan GodMode!</h3>
&nbsp;&nbsp;&nbsp;&nbsp;GodMode adalah gerbang untuk menguasai Windows seutuhnya. Didalamnya kamu bakal mendapati semua tool dan opsi yang bisa kamu gunakan untuk mengatur Windows sesuai keinginanmu.<br />
Untuk mengaktifkan GodMode, cukup buat folder di desktop dan beri nama GodMode.{ED7BA470-8E54-465E-825C-99712043E01C}
Setelah kamu Enter maka sebuah shortcut menuju penguasaan Windows secara penuh bakal kamu dapatkan.<br />
<img src="Image/other/tweakwindows10_4.jpeg" id="j" /><br /><br />
<h3>5. Gunakan Modern Office Touch</h3>
&nbsp;&nbsp;&nbsp;&nbsp;OS modern pas banget kalau dipadukan dengan Office modern. Untuk itu install dan gunakan Office Touch untuk semakin melengkapi aplikasi Modern di Windows 10 kamu.<br />
<img src="Image/other/tweakwindows10_5.jpeg" id="j" /><br /><br /> 
<h3>6. Aktifkan Fitur Tersembunyi di Command Prompt</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Command Prompt Windows 10 telah dilengkapi fitur tersembunyi yang paling banyak diinginkan oleh pengguna Windows sepanjang masa. Salah satunya adalah fitur Copy dan Paste didalam Command Prompt dengan Ctrl + C dan Ctrl + V.<br />
Untuk mengaktifkan fitur ini, cukup klik kanan bar Command Prompt, lalu pilih Properties. Kamu bisa mengaktifkan fitur baru tersebut dibawah tab Experimental.<br />
<img src="Image/other/tweakwindows10_6.jpeg" id="j" /><br /><br />
<h3>7. Mematikan Quick Access View!</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Di Windows 10 ketika kamu membuka File Explorer, maka kamu akan langsung diarahkan ke Quick Access View. Jujur aja WinPoin cukup senang dengan adanya fitur ini. Namun bagi kamu yang tidak menginginkannya dan ingin File Explorer tetap mengarah ke This PC seperti di Windows 8, matikan saja melalui File Explorer > View > Options > Ubah Open File Explorer to dari Quick Access menjadi This PC.<br />
<img src="Image/other/tweakwindows10_7.jpeg"  id="j" /><br /><br />
<h3>8. Coba Shortcut Baru!</h3>
Windows 10 dilengkapi dengan berbagai shortcut keyboard baru yang menarik untuk kamu coba, antara lain:
•	Snap Windows: klik sebuah window aktif, lalu tekan Win + Left atau Right<br />
•	Pindah window: Alt + Tab, jika Alt ditahan maka akan ke view window, tetapi jika dilepas akan berganti ke window lainnya<br />
•	Task view: Win + Tab<br />
•	Membuat virtual desktop baru: Win + Ctrl + D<br />
•	Menutup virtual desktop: Win + Ctrl + F4<br />
•	Berpindah virtual desktop: Win + Ctrl + Left atau Right<br /><br />
<h3>9. Memindahkan Apps ke Virtual Desktop</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Dengan adanya virtual desktop, maka kini kamu bisa memindahkan Apps dari satu desktop ke virtual desktop lainnya. Caranya cukup klik saja tombol Task View di taskbar, lalu klik kanan window yang ingin dipindah dan klik Move to > Desktop yang diinginkan<br />
<img src="Image/other/tweakwindows10_8.jpeg" id="j" /><br /><br />
<h3>10. Matikan Auto-Enhance di Photo</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Aplikasi Photo di Windows 10 dilengkapi fitur auto-enhance yang bakal memperbaiki foto secara otomatis jika dianggap kurang. Namun kalau kamu tidak ingin foto kamu “disentuh” tanpa ijin oleh aplikasi ini, matikan saja fitur auto-enhance.<br />
Caranya tinggal buka Photo App > Settings > Viewing and Editing > Off<br />
<img src="Image/other/tweakwindows10_9.jpeg" id="j" /><br /><br />
<h3>11. Maps Offline!</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Ini nih fitur yang menurut WinPoin paling keren di Windows 10, Offline Maps! Kini kamu bisa mendownload Maps dan mengaksesnya tanpa koneksi internet sekalipun. Jadi nanti ketika tersesat di perjalanan, gak ada sinyal wifi atau gak dapet koneksi internet, tinggal buka Offline Maps dan semua masalah beres.
Namun untuk bisa mengakses Maps secara Offline, kamu harus mendownloadnya terlebih dahulu. Caranya buka Maps > Setttings > Download or update maps.
<img src="Image/other/tweakwindows10_10.jpeg" id="j" /><br /><br />
<h3>12. Bersihkan Taskbar</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Windows 10 dilengkapi Cortana, itu keren! Tetapi masalahnya dia dalam bentuk form pencarian di taskbar dan itu bikin taskbar penuh. Lalu bagaimana dong?<br />
Tenang. Kamu bisa membersihkan Taskbar dengan mengubah form pencarian menjadi Icon Cortana. Caranya klik kanan saja taskbar dan ubah Search > Search Icon.<br />
<img src="Image/other/tweakwindows10_11.jpeg" id="j" /><br /><br />
<h3>13. Aktifkan Mode Tablet!</h3>
Meskipun kamu menggunakan PC, tidak ada salahnya kamu bermain-main dengan mode tablet. Aktifkan saja mode tablet dan mari bersenang-senang di mode ini!<br />
Caranya cukup klik icon Notifikasi, lalu aktifkan Tablet mode.<br />
