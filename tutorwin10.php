<div id="hh1">Cara Install Windows 10 dengan Mudah</div>
<div id="b">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Silahkan restart PC kamu lalu jika sudah memulai start, tekan hotkey untuk masuk ke dalam sistem BIOS. Hotkey tersebut bisa kamu perhatikan di bagian pojok kanan bawah PC kamu. Apakah bios itu? Secara kasar bisa disimpulkan bahwa BIOS itu sejenis pengaturan awal sebelum kamu melakukan booting ke PC.  Secara default, hotkey untuk memasuki BIOS ada bermacam-macam mulai dari F2 , F4, F10, F8, F12 dan DEL tergantung dari merk PC masing-masing. Usahakan agak cepetan nekannya agar proses ini tidak terlewatkan. Jika terlambat, silahkan ulang restart kembali. Untuk proses ini pastikan flashdisk kamu dalam keadaan tercolok.<br /><br />
<img src="Image/software/asus.jpg" id="j"><br />
Selanjutnya, jika sudah berhasil memasuki BIOS pasti kamu akan menjumpai tampilan yang berwarna biru-biru. Lalu, masuk ke tab Boot dan cari boot options/boot order. Setting boot order kamu agar pilihan Removeable Disk (jika tidak ada, cari saja nama merk flashdisk kamu contoh : Kingston atau yang lain) kamu bisa berada paling atas. Disusul oleh Harddisk/HardDrive (jika tidak ada cari yang ada embel-embel HDD atau merk harddisk kamu contoh : Hitachi) dipilihan kedua.<br /><br />
Jika sudah tersusun seperti itu, lakukan save and exit dengan tombol F4 atau F10 (tergantung dari merk BIOSnya) silahkan baca aturan key-nya disebelah pojok kanan bawah.<br/><br />
<img src="Image/software/bios.jpg" id="j"><br />
Setelah proses save selesai, maka PC kamu akan kembali restart. Namun, yang akan kamu jumpai setelah ini adalah logo installer Windows 10. Hmm, sudah mulai nih proses seriusnya hehe.<br /><br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-51-33-1024x768.png" id="j"><br />
Sampai disini, pilih bahasa yang mau kamu gunakan. Selebihnya biarkan saja. Lalu klik install now<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-51-57-1024x768.png" id="j"><br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-52-06-1024x768.png" id="j"><br />
Nah sekarang kamu akan dihadapkan dengan inputan serial. JANGAN PANIK !! Kamu nggak perlu cari cari serial lagi. Cukup di skip saja hehe. Harus jeli dong nyari tulisan skipnya :p<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-52-35-1024x768.png" id="j" /><br />
Selanjutnya ada 2 pilihan instalasi. Pilih saja costum. Daan, kamu akan menuju pengaturan partisi. Untuk sesi ini harap diperhatikan baik-baik. Kamu akan menjumpai partisi-partisi apa saja yang akan ada di PC kamu. Sekarang, select drive C kamu. JANGAN SAMPAI SALAH atau data-data kamu yang di partisi D pun juga akan lenyap. Dalam screenshot saya dibawah ini, kebetulan size drive C saya adalah 20GB. Maka, itulah yang akan saya format. Pilih menu format lalu yes. Tunggu sebentar, kemudian pastikan drive yang sedang terselect / dipilih adalah drive C tadi. Lalu silahkan klik next untuk melanjutkan, jangan sampai salah.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-53-04-1024x768.png" id="j"><br />
<img src="Image/software/select-1024x768.jpg" id="j"><br />
Proses instalasi sedang berlangsung. Proses ini akan memakan waktu hingga sekitar 20-30 menit.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-19-55-40-1024x768.png" id="j"><br />
Jika sudah, pc kamu akan otomatis melakukan restart. PENTING!! Saat melakukan proses restart dan sudah muncul logo merk PC/laptop kamu langsung cabut flashdisknya !! Atau proses instalasi akan kembali diulang. Ribet kan kalau ngulang lagi hehe.<br /><br />
Tunggu proses restart hingga lebih kurang 10 menit , jika sudah maka akan kembali otomatis restart kembali. Setelah itu , kamu akan kembali lagi dihadapkan dengan pilihan serial. Wah-wah, ngotot bener ya pihak microsoftnya.<br /><br />
Pilih saja do this later lalu akan diarahkan ke menu settings. Pilih tombol Use Express Settings agar nggak ribet lagi hehe. Tunggu sebentar hingga proses automatic settings selesai.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-20-20-48-1024x768.png" id="j"><br />
Selanjutnya, ada lagi pertanyaan “Who owns this PC?” Pilih saja I own it lalu next.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-20-25-05-1024x768.png" id="j"><br />
Sekarang, kamu akan diminta login dengan microsoft account. Cukup di skip aja, kalau mau login cukup dilakukan nanti setelah semuanya selesai.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-20-26-12-1024x768.png" id="j"><br />
Di bagian Create User Accounts, isikan nama kamu dan juga password. Jika tidak mau berpassword, cukup kosongkan saja lalu next.<br />
<img src="Image/software/Win-10-BAGAS31-2015-07-21-20-26-26-1024x768.png" id="j"><br />
