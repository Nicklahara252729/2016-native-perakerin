<div id="hh1">Cara Jailbreak iOS 8.1 iPhone iPod iPad dengan Pangu8</div>
<div id="b">
Bagi pengguna iOS 8 khususnya iOS 8.1 untuk iPhone iPod iPad sudah bisa di jailbreak dengan tools pangu8. Pangu8 merupakan lanjutan dari Pangu7 yang sebelumnya sudah dirilis untuk jailbreak iOS 7.1.2. Kini dengan Pangu8 v1.2.1 untuk pengguna windows (untuk pengguna mac menyusul, kata team pangu.io) sudah tersedia dalam bahasa inggris dan juga sudah include Cydia app yang kompatibel untuk iOS 8 sampai iOS 8.1.<br /><br />

Dan berikut tutorial cara jailbreak iPhone iPod iPad pada iOS 8.0 – 8.1 dengan menggunakan tool Pangu 8:<br /><br />
<b>Compatible devices iOS 8/8.1</b>

   - iPhone 6 Plus, iPhone 6, iPhone 5s, iPhone 5c, iPhone 5, iPhone 4s<br />
   - iPad Air, iPad Mini, iPad 4, iPad 3 and iPad 2<br />
   - iPod Touch 5G<br /><br />

<b>Notes:</b><br />

   - Backup idevice (utamakan keselamatan data idevice)<br />
   - iTunes versi terakhir<br />
   - iOS 8 yang sudah restore (fresh update) via itunes<br />

