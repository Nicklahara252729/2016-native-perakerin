<h3>1. Dirilis di 190 Negara Dengan 111 Bahasa</h3>
Fakta pertama dari Windows 10 yang akan rilis pada 29 Juli 2015 mendatang adalah perilisan yang diperuntukkan bagi 190 negara dengan daya dukungan 111 bahasa. Hal ini sendiri sudah dikonfirmasi oleh pihak Microsoft melalui blog resminya.
<br /><br />
<h3>2. Bisa Jalankan Aplikasi Android Dan iOS</h3>
Untuk lebih menyenangkan para penggunanya, pihak Microsoft telah menyatakan bahwa Windows 10 nantinya akan mampu menjalankan aplikasi #Android dan iOS. Pada acara Build 2015 yang berlangsung di San Fransisco, Amerika Serikat, awal Mei kemarin, Microsoft menyatakan bahwa dengan menggunakan kode yang sudah ada di dalam sistem operasi pada wadah keamanan khusus, aplikasi Android dan iOS yang akan mampu dijalankan di Windows 10.
Dengan adanya kemudahan ini diharapkan Microsoft bisa mendongkrak jumlah #aplikasi yang tersedia pada Windows Store.
<br /><br />
<h3>3. Windows 10 Akan Jadi Generasi Terakhir</h3>
Kabar mencengangkan juga terdengar dari #sistem operasi Windows 10 yang pernah diungkapkan oleh Jerry Nixon, developer evangelist dari Microsoft pada gelaran Microsoft Ignite yang berlangsung 4-8 Mei yang lalu. Menurut Jerry Nixon, Windoes 10 yang akan dirilis pada 29 Juli 2015 nanti akan menjadi “generasi terakhir” dari sistem operasi Windows yang selama ini mereka andalkan.<br /><br />
Saat itu Nixon menyatakan bahwa sistem operasi Windows 10 rencananya akan digunakan sebagai “nama abadi”  produk sistem operasi dari Microsoft. Ini artinya Windows tidak akan pernah punah, karena namanya telah menjadi sejarah yang terpampang pada “generasi terakhirnya” yaitu Windoes 10.
Saat Microsoft tak memperpanjang sistem operasi Windows, pihaknya akan menggunakan sistem penamaan (branding) sistem operasi seperti Apple yang selama 15 tahun terakhir telah sukses mengusung brand OS X untuk sistem operasi jajaran produk desktop dan laptopnya.<br /><br />
<h3>4. Ada 6 Varian Windows 10</h3>
Fakta berikutnya Windows 10 adalah munculnya enam varian yang akan ada pada sistem operasi terbaru Microsoft tersebut. Enam varian dari windows 10 tersebut antara lain Windows 10 Home, Windows 10 Pro, Windows 10 Enterprise, Windows 10 Mobile, Windows 10 Education, serta Windows 10 Mobile Enterprise. Dengan adanya enam varian ini pihak Microsoft berharap bisa menarik konsumen dan membuat penjualannya meningkat lebih tinggi lagi dari produk sebelumnya windows 8.<br /><br />
<h3>5. Tidak Sepenuhnya Gratis</h3>
Seperti telah dijelaskan sebelumnya bahwa Windows 10 akan ditawarkan secara gratis sebagai update sistem operasi  bagi pengguna Operating System (OS) resmi Windows 7, Windows 8.1 serta Windows Phone terbaru. Pada kenyataannya update gratis ini hanya berlaku untuk pemakaian 1 tahun pertama, dan ini artinya akan ada biaya yang harus dikeluarkan pengguna di tahun berikutnya.<br /><br />
Bagi pengguna baru windows, mereka bisa mendapatkan sistem operasi terbaru Windows 10 dengan harga 119 dollar AS atau sekitar Rp 1,6 juta untuk Windows 10 versi Home dan 199 dollar AS atau sekitar Rp 2,6 juta untuk Windows 10 Professional.<br /><br />
<h3>6. Hilangnya Beberapa Aplikasi Saat Upgrade</h3>
Saat pengguna melakukan upgrade, OS baru ini akan menghilangkan beberapa program perangkat lunak seperti Windows Media Center, game Hearts, dan Windows 7 desktop gadget. Bagi yang pengguna yang masih menggunakan floppy disk, mereka juga diharuskan menginstal driver baru dan bagi Anda yang hobi menonton DVD, mereka juga diharuskan memasang “perangkat lunak terpisah” untuk memutar DVD tersebut.<br /><br />
<h3>7. Munculnya Fitur Baru</h3>
Terakhir, fakta yang ada pada Windows 10 adalah munculnya sejumlah fitur baru bagi para pengguna sistem operasi tersebut seperti peramban Microsoft Edge dan #teknologi personal assistant Cortana. Sistem operasi terbaru ini akan menghadirkan kembali menu Start tradisional setelah sebelumnya sempat ditiadakan pada Windows 8.
