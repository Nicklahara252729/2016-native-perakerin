<h3>Latar belakang</h3>
&nbsp;&nbsp;&nbsp;&nbsp;Serikat Karyawan Telkom, atau disingkat SEKAR (berdiri 1 Maret 2000) adalah sebuah organisasi bersifat demokratis, mandiri, bebas, bertanggungjawab, terbuka (stelsel aktif) serta tidak berafiliasi politis kepada orgaisasi apapun yang beranggotakan ±90% karyawan PT Telkom.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Kelahiran Serikat Karyawan PT Telekomunikasi Indonesia, Tbk (Sekar TELKOM) tidak dapat dipisahkan dari bergulirnya era reformasi, medio 1998. Era demokrasi, era kebebasan berkumpul dan berserikat.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Pembubaran Korps Pegawai Republik Indonesia atau KORPRI PT TELKOM pada tahun 1999 mengawali perjalanan bersejarah lahirnya Sekar TELKOM. Pada akhir bulan Februari 2000, segenap karyawan TELKOM dari seluruh unit kerja di seluruh Indonesia berkumpul di Bandung melaksanakan Musyawarah Karyawan (MUKAR) pembentukan wadah bersatunya karyawan. Sebelum itu, embrio sangat awal dari Sekar Telkom diawali dari segenap karyawan di Kalimantan dalam wadah Serikat Pekerja Telkom Kalimantan (SPTK) pada tahun 1999.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Pada awalnya, nama yang disetujui adalah Paguyuban Karyawan TELKOM (PAKAR). Namun demikian aspirasi & kebulatan tekad wakil-wakil karyawan yang hadir pada MUKAR tersebut, akhirnya menyepakati sebuah bentuk organisasi wadah karyawan dengan nama Serikat Karyawan PT Telekomunikasi Indonesia,Tbk atau disingkat Sekar TELKOM. Dan karena pendeklarasian Sekar TELKOM jatuh pada tanggal 29 Februari 2000 yang merupakan tahun kabisat, maka MUKAR menetapkan satu hari sesudahnya yaitu 1 Maret 2000 sebagai hari deklarasi Sekar TELKOM. Yang selanjutnya diperingati sebagai hari lahir Sekar TELKOM.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;SEKAR telah mencatat beberapa aktivitas, mulai dari lingkup internal perusahaan Telkom sampai pada lingkup nasional. SEKAR boleh dibilang ikut menjaga keutuhan dan komitmen untuk menjadikan Telkom sebagai flag carrier telekomunikasi di Indonesia, antara lain mempertahankan Telkom sebagai satu-satunya BUMN di bidang telekomunikasi.
Tiga tahun pertama semenjak kelahiran Sekar TELKOM adalah tahun penuh tantangan dan bahkan ancaman kepada eksistensi TELKOM atau tahun-tahun vivere pericoloso. Gelombang aksi unjuk rasa Sekar TELKOM mewarnai media massa lokal maupun nasional sebagai protes atas buruknya kinerja KSO-3 Jabar dan rencana pengalihan Divre-4 Jateng-DIY kepada Indosat.<br /><br />
Sekar TELKOM pun telah memancangkan empat pilar peran kesejarahan sebagai haluan organisasi yaitu :<br />
•	Sebagai wadah pemersatu karyawan;<br />
•	Sebagai wadah aspirasi karyawan;<br />
•	Sebagai mitra konstruktif manajemen;<br />
•	Sebagai pengawal dan penegak Good Corporate Governance (Bersih, &nbsp;&nbsp;&nbsp;&nbsp;Transparan dan Profesional).<br />
Dalam strategi perjuangannya SEKAR membentuk poros aliansi dengan serikat karyawan/pegawai dari BUMN-BUMN strategis lainnya, misal dengan :<br />
•	Serikat Pegawai PLN,<br />
•	Serikat Pegawai Pertamina,<br />
•	Serikat Pegawai Telkomsel;<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Guna lebih vokal dalam memperjuangkan kepentingan "Merah Putih". Aliansi tersebut berhasil membatalkan penjualan Divisi Regional Telekomunikasi IV Jawa Tengah kepada pihak swasta asing, menyelamatkan penjualan Pertamina, PLN.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;Dalam usianya yang relatif muda, SEKAR mendirikan Yayasan Sekar Peduli, suatu manfestasi kepedulian karyawan Telkom terhadap bebagai persoalan sosial kemasyarakatan Indonesia. Aktivitasnya dipicu oleh terjadinya gempa bumi Samudra Hindia 2004. SEKAR telah memberikan berbagai bantuan, antara lain : Penyediaan klinik berjalan dan mobil jenazah gratis yang bekerjasama dengan DSUQ, Bantuan kesehatan di daerah bencana bekerjasama dengan Yasasan Kemuning, Serta aktivitas SAR, Sekar TELKOM juga telah membentuk Yayasan Sekar TELKOM (YST) yg telah berkiprah dalam aksi kepedulian sosial berbagai bencana (Tsunami Aceh-Nias Des 2004, banjir di Jember, Gempa bumi Yogyakarta Mei 2006, Tsunami Pantai Selatan Oktober 2006 & banjir besar di Jakarta awal Februari 2007).<br /><br />
