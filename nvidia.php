<img src="Image/other/nvidia-geforce-gtx-titan-z-03.jpg" id="j"><br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tentu saja dengan harga jual fantastis seperti itu, GeForce GTX TITAN Z tidak ditujukan untuk kalangan gamer atau pengguna PC pada umumnya. GeForce GTX TITAN Z lebih ditujukan untuk kalangan pengguna PC dengan kebutuhan komputasi tinggi pada bidang seperti computer aided design (CAD), defense and intelligence, media and entertaintment, medical imaging, physics, oil and gas/seismic, wheater climate, dan masih banyak lagi. Seperti kita ketahui, lini GeForce GTX TITAN Series memiliki keistimewaan pada kemampuan komputasi double precision dimana kemampuan seperti itu biasa ditemukan pada graphics card NVIDIA Tesla. GeForce GTX TITAN Z diklaim memiliki kemampuan komputasi hingga 8 TeraFLOPS.<br/><br />
