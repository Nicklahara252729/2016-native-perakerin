<h3>PT Multimedia Nusantara</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;100% saham Multimedia Nusantara dimiliki oleh Telkom. Multimedia Nusantarabergerak di bidang multimedia. Multimedia Nusantara diarahkan sebagai pelaksana amanat strategis Telkom dalam menjalankan bisnis di bidang telco adjacent industries dengan visi sebagai pemimpin masa depan dalam industri. TelkomMetra menargetkan pendapatan usaha senilai Rp10 triliyun pada tahun 2015.<br />
<h5><i>Mojopia</i></h5>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;100% saham Mojopia dimiliki oleh Multimedia Nusantara. MetraNet adalah nama lain dari Mojopia. Mojopia bergerak dalam membina dan membantu memasarkan produk Usaha Kecil dan Menengah (UKM) di Indonesia secara perdagangan elektronik.<br />
