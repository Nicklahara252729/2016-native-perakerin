<h5><i>PT Administrasi Medika (AdMedika)</i></h5>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;AdMedika didirikan pada tahun 2002 dan merupakan perusahaan third party administrator (TPA) pertama di Indonesia yang mengombinasikan aplikasi proses klaim kesehatan secara daring dengan layanan administrasi asuransi/jaminan kesehatan. Sejak tahun 2010, AdMedika resmi bergabung di Telkom Group dengan akuisisi melalui anak perusahaan Telkom yang bernama PT Multimedia Nusantara (Metra) dengan kepemilikan saham mayoritas 75%.<br />
<h5><i>PT Finnet Indonesia (Finnet)</i></h5>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;PT Finnet Indonesia adalah anak perusahaan Telkom yang bergerak di bidang sistem pembayaran elektronik. Finnet didirikan oleh Telkom dalam bentuk joint venture company antara anak perusahaan Telkom yaitu Multimedia Nusantara dengan PT Mekar Prana Indah (MPI) yang sahamnya dimiliki oleh Yayasan Kesejahteraan Karyawan Bank Indonesia (YKBI). Adapun komposisi kepemilikan antara Metra dan MPI adalah masing-masing 60% dan 40%. Pendirian Finnet adalah langkah nyata Telkom dalam rangka menangkap peluang pasar dalam menyediakan Layanan sistem pembayaran secara elektronik dengan menyediakan layanan solusi terpadu sistem pembayaran elektronik untuk perbankan atau semua sektor yang berkaitan dengan transaksi finansial elektronik.<br />
<h5><i>PT Melon Indonesia (Melon)</i></h5>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Melon merupakan perusahaan joint venture didirikan pada tanggal 16 Agustus 2010 oleh Multimedia Nusantara dan South Korea Telecom dengan komposisi saham 51% dan 49%. Perusahaan yang merupakan realisasi ekspansi di bisnis media pedidikan yang berfokus pada bisnis layanan musik digital dan konten lain untuk telepon seluler, komputer pribadi, kanal elektronik konsumen dan media digital lainnya.<br />
