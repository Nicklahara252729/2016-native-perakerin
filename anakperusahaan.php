<h3>PT Telekomunikasi Selular (Telkomsel)</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Saham Telkom 65% dimiliki oleh PT Telkom dan sisanya dimiliki oleh Singapore Telecommunications. Telkomsel merupakan anak perusahaan PT Telkom sebagai operator telekomunikasi seluler pertama di Indonesia yang berbasis teknologi jaringan GSM Dual Band (900 & 1800), GPRS, WiFi, EDGE, 3G, HSDPA dan HSPA di seluruh Indonesia. Untuk jaringan internasional, Telkomsel telah berkolaborasi dengan 362 roaming partners di 196 negara.<br />
<h5><i>Telkomsel Finance B.V (TFBV)</i></h5>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;TFBV merupakan anak perusahaan dari Telkomsel yang berada di Amsterdam, Belanda. Perusahaan ini didirikan dengan tujuan peminjaman dan pengumpulan dana, termasuk penerbitan obligasi, surat kesanggupan bayar dan surat berharga lainnya. Telkom memiliki saham TFBV melalui Telkomsel sebesar 65%.<br />
<h5><i>Telekomunikasi Selular Finance Limited (TSFL)</i></h5>
TSFL merupakan anak perusahaan Telkomsel yang didirikan pada tanggal 22 April 2002 dan bergerak di bidang investasi dan keuangan. Telkom menguasai 65% saham TSFL melalui Telkomsel.
