<h3>Era kolonial</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1882, didirikan sebuah badan usaha swasta penyedia layanan pos dan telegraf. Layanan komunikasi kemudian dikonsolidasikan oleh Pemerintah Hindia Belanda ke dalam jawatan Post Telegraaf Telefoon (PTT). Sebelumnya, pada tanggal 23 Oktober 1856, dimulai pengoperasian layanan jasa telegraf elektromagnetik pertama yang menghubungkan Jakarta (Batavia) dengan Bogor (Buitenzorg). Pada tahun 2009 momen tersebut dijadikan sebagai patokan hari lahir Telkom.<br /><br />
<h3>Perusahaan negara</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1961, status jawatan diubah menjadi Perusahaan Negara Pos dan Telekomunikasi (PN Postel). Kemudian pada tahun 1965, PN Postel dipecah menjadi Perusahaan Negara Pos dan Giro (PN Pos & Giro) dan Perusahaan Negara Telekomunikasi (PN Telekomunikasi).<br /><br />
<h3>Perumtel</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1974, PN Telekomunikasi diubah namanya menjadi Perusahaan Umum Telekomunikasi (Perumtel) yang menyelenggarakan jasa telekomunikasi nasional maupun internasional. Tahun 1980 seluruh saham PT Indonesian Satellite Corporation Tbk. (Indosat) diambil alih oleh pemerintah RI menjadi Badan Usaha Milik Negara (BUMN) untuk menyelenggarakan jasa telekomunikasi internasional, terpisah dari Perumtel. Pada tahun 1989, ditetapkan Undang-undang Nomor 3 Tahun 1989 tentang Telekomunikasi, yang juga mengatur peran swasta dalam penyelenggaraan telekomunikasi.<br /><br />
<h3>PT Telekomunikasi Indonesia (Persero)</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1991 Perumtel berubah bentuk menjadi Perusahaan Perseroan (Persero) Telekomunikasi Indonesia berdasarkan Peraturan Pemerintah Nomor 25 Tahun 1991.<br /><br />
<h3>PT Telekomunikasi Indonesia (Persero) Tbk</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tanggal 14 November 1995 dilakukan Penawaran Umum Perdana saham Telkom. Sejak itu saham Telkom tercatat dan diperdagangkan di Bursa Efek Jakarta (BEJ/JSX) dan Bursa Efek Surabaya (BES/SSX) (keduanya sekarang bernama Bursa Efek Indonesia (BEI/IDX), Bursa Efek New York (NYSE) (Diperdagangkan pada tanggal 14 Juli 2003) dan Bursa Efek London (LSE). Saham Telkom juga diperdagangkan tanpa pencatatan di Bursa Saham Tokyo. Jumlah saham yang dilepas saat itu adalah 933 juta lembar saham. Sejak 16 Mei 2014, saham Telkom tidak lagi diperdagangkan di Bursa Efek Tokyo (TSE) dan pada 5 Juni 2014 di Bursa Efek London (LSE).<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tahun 1999 ditetapkan Undang-undang Nomor 36 Tahun 1999 tentang Telekomunikasi. Sejak tahun 1989, Pemerintah Indonesia melakukan deregulasi di sektor telekomunikasi dengan membuka kompetisi pasar bebas. Dengan demikian, Telkom tidak lagi memonopoli telekomunikasi Indonesia.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tahun 2001 Telkom membeli 35% saham Telkomsel dari PT Indosat sebagai bagian dari implementasi restrukturisasi industri jasa telekomunikasi di Indonesia yang ditandai dengan penghapusan kepemilikan bersama dan kepemilikan silang antara Telkom dan Indosat. Sejak bulan Agustus 2002 terjadi duopoli penyelenggaraan telekomunikasi lokal.Pada 17 Oktober 2009, Telkom meluncurkan <i>"New Telkom" ("Telkom baru")</i> yang ditandai dengan penggantian identitas perusahaan.
