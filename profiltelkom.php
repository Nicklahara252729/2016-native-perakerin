&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;PT Telekomunikasi Indonesia Tbk (Persero) biasa disebut Telkom Indonesia atau Telkom saja (IDX: TLKM, NYSE: TLK, LSE: TKID) adalah perusahaan informasi dan komunikasi serta penyedia jasa dan jaringan telekomunikasi secara lengkap di Indonesia. Telkom mengklaim sebagai perusahaan telekomunikasi terbesar di Indonesia, dengan jumlah pelanggan telepon tetap sebanyak 15 juta dan pelanggan telepon seluler sebanyak 104 juta.
