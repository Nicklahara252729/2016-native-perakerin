<h3>PT Graha Sarana Duta (TelkomProperty/GSD)</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;GSD merupakan sebuah perusahaan properti terpadu yang dimiliki oleh Telkom pada tahun 2001, dengan porsi kepemilikan saham Telkom sebesar 99,99%.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;GSD didirikan pada tanggal 30 September 1981 sebagai Graha Sarana Duta untuk menyediakan Office Building, Jasa Pemeliharaan dan Perawatan Gedung Bank Duta (pada saat itu). Sejalan dengan perkembangan bisnis perusahaan, Perseroan kemudian mengembangkan portofolio ke bidang Jasa Konstruksi dan dipercaya untuk membangun beberapa kantor cabang Bank Duta dan Bank Bukopin serta sebuah Gedung Kampus YAI di Jalan Salemba, Jakarta.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tanggal 25 April 2001, kepemilikan Graha Sarana Duta diambil alih sepenuhnya oleh Telkom untuk mengelola gedung-gedung kantor dan asset properti Telkom, yang sebelumnya dikelola oleh Divisi Properti Telkom. Di bawah kendali Telkom, GSD terus berkembang menjadi perusahaan properti yang terpadu (integrated property development) dan kini memiliki tiga portofolio bisnis yaitu :<br /><br />
•Property Services antara lain Building Management, Partial Property Services, Office Space Leasing, &nbsp;Security Management dan Space & Occupancy Management.<br />
•Project Management seperti Office Fit Out & Interior, Building Renovation dan Construction<br />
•Property Development & Investment untuk Office Buildings, Residential Estates dan Technical Building.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Selama tiga puluh tahun sejak didirikan oleh PT Bank Duta pada tahun 1981, Perseroan menggunakan nama belakang ‘Duta’ yang diadopsi dari nama PT Bank Duta dan menggunakan logo Perseroan yang diciptakan oleh PT Bank Duta. Pada tahun 2011, manajemen Perseroan memutuskan untuk melakukan pencitraan kembali perusahaan (corporate rebranding) dilatar belakangi oleh alasan – alasan sebagai berikut :<br /><br />
•Perubahan Visi dan Misi Perseroan pada tahun 2010<br />
•Perubahan Portofolio Perseroan menjadi Perusahaan Properti Terpadu (Integrated Property Development)<br />
•Pencitraan yang Ingin Dibangun Perseroan; bahwa manajemen Perseroan berkomitmen untuk melakukan &nbsp;transformasi bisnis perusahaan dalam aspek kinerja, kultur, dan kompetensi internal perusahaan, untuk &nbsp;dapat bersaing dengan pelaku bisnis lainnya di industri properti Indonesia<br />
•Tanggal atau Momen Tertentu; pada tahun 2011, Perseroan merayakan ulang tahun ke-30 (ke tiga puluh) &nbsp;sejak tanggal pendirian perusahaan pada 30 September 1981 dan 10 (sepuluh) tahun kepemilikan GSD &nbsp;oleh Telkom sejak tanggal 25 April 2001.<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Saat ini, GSD memiliki cakupan wilayah kerja di seluruh Indonesia dan melakukan pengelolaan terhadap gedung-gedung perusahaan Telkom Group seperti gedung PT Telekomunikasi Indonesia, PT Telkomsel, PT Infomedia Nusantara dan PT Multimedia Nusantara. Selain itu, GSD juga mengelola 106 lokasi gedung lain yang dimiliki oleh berbagai bidang usaha di luar Telkom Group seperti perkantoran, apartemen, mall, dan bandara baik secara keseluruhan maupun secara parsial.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sejak tahun 2012, Telkom mengubah nama Graha Sarana Duta menjadi TelkomProperty sebagai nama anak perusahaan baru Telkom bidang Properti.<br /><br />
