&nbsp;&nbsp;&nbsp;&nbsp;Indonesia Digital HOME (disingkat IndiHOME) adalah salah satu produk layanan dari PT Telekomunikasi Indonesia (Telkom) berupa paket layanan komunikasi dan data seperti telepon rumah (voice), internet (Internet on Fiber atau High Speed Internet), dan layanan televisi interaktif (USee TV Cable, IP TV). Paket IndiHome juga dilengkapi dengan konten seperti layanan portal musik digital dan Home Automation. Sejak diluncurkannya IndiHOME, pelanggan berlangganan paket internet Speedy satu-per-satu diminta untuk pindah ke IndiHOME, karena layanan dagang Speedy akan segera diberhentikan di tahun 2015.<br><br>
