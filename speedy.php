&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Speedy adalah penyelenggara jasa Internet yang dimiliki oleh Telkom Indonesia. Speedy berbasis teknologi akses asymmetric digital subscriber line (ADSL) dan gigabit passive optical network (GPON)[1] dengan menggunakan jaringan fiber optik sampai ke rumah atau Fiber to the home (FTTH). Berbeda dengan layanan Internet dengan koneksi akses Internet putar-nomor seperti TelkomNet Instan, Speedy menggunakan saluran telepon yang dapat dipergunakan untuk menelepon bersamaan dengan mengakses Internet. Untuk dapat menikmati fasilitas menelepon dan internet secara simultan, pelanggan Speedy harus menggunakan splitter yang dapat memisahkan saluran telepon dan saluran modem. Selain itu, menyepakati ketentuan yang berlaku di Plasa Telkom setempat. Telkom Indonesia menghadirkan layanan IndiHome berbasis triple-play[2]. IndiHome menghadirkan layanan akses internet melalui jalur telepon dan fiber optik, VoIP sebagai layanan telepon berbasis internet, dan IPTV sebagai layanan televisi digital.
