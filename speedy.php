<h3>Perusahaan negara</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1961, status jawatan diubah menjadi Perusahaan Negara Pos dan Telekomunikasi (PN Postel). Kemudian pada tahun 1965, PN Postel dipecah menjadi Perusahaan Negara Pos dan Giro (PN Pos & Giro) dan Perusahaan Negara Telekomunikasi (PN Telekomunikasi).<br /><br />
<h3>ONT</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Salah satu ONT (Optical Network Terminal) yang diberikan Telkom saat berlangganan produk Telkom (Telepon Rumah, Speedy, UseeTV). bermerek Huawei model HG8245A
Selain modem ADSL, Telkom Speedy juga menyediakan ONT khusus untuk pengguna Speedy GPON. ONT ini diberikan oleh Telkom ke pelanggannya apabila jaringan pelanggan sudah menggunakan teknologi GPON.
<br /><br />
<h3>Koneksi</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Karena teknologi yang digunakan hanya dapat menerima jaringan telepon tertentu saja, maka untuk dapat melakukan koneksi ADSL diperlukan kualitas jaringan telepon yang baik dan stabil serta memiliki alat digital subscriber line access multiplexer (DSLAM) yang diperoleh di Sentra Telepon Otomatis (STO) seluruh Indonesia dan Optical Line Terminal yang akan diperoleh di STO seluruh Indonesia.<br /><br />
Proses koneksi Internet dengan modem ADSL memerlukan 2 tahap:<br>
•	Koneksi ADSL dari modem ke DSLAM di STO terdekat.<br />
•	Koneksi Internet melalui broadband remote access server yang terlebih dahulu melalui proses otentikasi &nbsp;&nbsp;di remote authentication dial in user rerver untuk verifikasi Nomor Speedy dan kata sandi pelanggan &nbsp;&nbsp;Speedy. <br />
Lembaran kertas yang berisi nomor berlangganan Speedy dan kata sandi yang diberikan harap disimpan baik-baik atau langsung dicatat sebagai cadangan demi mencegah kehilangan data penting tersebut.<br />
Proses koneksi Internet dengan ONT GPON juga memerlukan 2 tahap:<br />
•	Koneksi GPON dari ONT ke ODP (Optical Distribution Point), kemudian dari ODP ke ODC (Optical &nbsp;&nbsp;&nbsp;Distribution Cabinet), dan dari ODC ke OLT (Optical Light Terminal) di STO Terdekat<br />
•	Di dalam OLT. sudah tersimpan data pelanggan. Sehingga OLT hanya perlu mencocokkan ONT yang &nbsp;&nbsp;terkoneksi di dalam jaringan GPON dengan data pelanggan. Jika cocok, maka dari OLT dialihkan ke &nbsp;&nbsp;&nbsp;jaringan Internet Telkom<br />
Paket internet berkecepatan 512 kbps hingga 5 mbps menggunakan jaringan telepon (ADSL) sebagai akses internet, sedangkan paket berkecepatan 10 mbps hingga 100 mbps menggunakan teknologi GPON.
