<h3>Keunggulan</h3>
•	Pelanggan dapat terhubung ke Internet, dan tetap dapat menggunakan telepon untuk menerima atau &nbsp;&nbsp;menelepon. Jika akses menelepon diaktifkan, maka ada iuran bulanan dibebankan kepada pelanggan. &nbsp;&nbsp;Kalau dinonaktifkan fasilitas menelepon tapi masih bisa mnerima telepon, maka biaya tambahan per &nbsp;&nbsp;&nbsp;bulan tersebut ditiadakan.<br />
•	Bagi pelanggan lama, Speedy tidak memerlukan kabel telepon baru. Proses pemasangan cukup &nbsp;&nbsp;&nbsp;&nbsp;menyambung kabel telepon yang telah ada ke modem.<br />
•	Akses Internet menggunakan Speedy di Indonesia kecepatannya semakin tinggi jika kabel serat optik &nbsp;&nbsp;&nbsp;telah merata digunakan di seluruh wilayah Indonesia.<br />
<h3>Kelemahan</h3>
•	Speedy akan bekerja dengan sempurna jika lokasi cukup dekat dengan sentral telepon. Dalam jarak 0-2 &nbsp;&nbsp;&nbsp;km bentangan kabel, ADSL2+ yang dipakai oleh Speedy dapat mencapai kecepatan sekitar 24 mbps akan &nbsp;&nbsp;&nbsp;stabil kecepatannya. Kualitas kecepatan semakin menurun jika kabel tembaga telah berusia tua dan jarak &nbsp;&nbsp;&nbsp;DSLAM terlalu jauh dari rumah pelanggan. Kabel serat optik dapat menetralisir kekurangan ini, namun &nbsp;&nbsp;&nbsp;sedang dalam pembangunan di seluruh wilayah Indonesia.<br />
•	Speedy lebih cepat menerima data daripada mengirim data melalui Internet.<br />
•	Layanan Speedy tidak ada di wilayah yang tidak ada jaringan telepon yang disediakan Telkom.
